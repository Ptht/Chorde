    def getNote(self, freq :float) -> (str, int, float):
        low = (freq + freq * 2**(-1/12)) / 2
        high = (freq + freq * 2**(1/12)) / 2
        for x in self.notes:
            if low < x[2] and x[2] < high:
                return (x[0], x[1], round(x[2], 1))
    